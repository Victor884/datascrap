;WITH
MEU AS
(
    SELECT
        CODFILIAL = CAST(CODFILIAL AS smallint),
        CHAPA = CAST(CHAPA AS varchar(16)),
        CODEVENTO = CAST(CODEVENTO AS varchar(10)),
        COMPETENCIA = CAST(COMPETENCIA AS date),
        VALOR_HORA = TRY_CONVERT(decimal(19,6), REPLACE(CAST(VALOR_HORA AS varchar(50)), ',', '.')),
        VALOR_MENSAL = TRY_CONVERT(decimal(19,6), REPLACE(CAST(VALOR_MENSAL AS varchar(50)), ',', '.')),
        FONTE_DEBUG
    FROM #MEU_SALARIO_COMPOSTO
),
ORI AS
(
    SELECT
        CODFILIAL = CAST(CODFILIAL AS smallint),
        CHAPA = CAST(CHAPA AS varchar(16)),
        CODEVENTO = CAST(CODEVENTO AS varchar(10)),
        COMPETENCIA = CAST(COMPETENCIA AS date),
        VALOR_HORA = TRY_CONVERT(decimal(19,6), REPLACE(CAST(VALOR_HORA AS varchar(50)), ',', '.')),
        VALOR_MENSAL = TRY_CONVERT(decimal(19,6), REPLACE(CAST(VALOR_MENSAL AS varchar(50)), ',', '.'))
    FROM [GestaoBI].dbo.[AIN_SALARIO_COMPOSTO] WITH (NOLOCK)
    WHERE CAST(COMPETENCIA AS date) = @COMPETENCIA
),
CHAVES AS
(
    SELECT
        CODFILIAL = COALESCE(M.CODFILIAL, O.CODFILIAL),
        CHAPA = COALESCE(M.CHAPA, O.CHAPA),
        CODEVENTO = COALESCE(M.CODEVENTO, O.CODEVENTO),
        O_VALOR_HORA = O.VALOR_HORA,
        O_VALOR_MENSAL = O.VALOR_MENSAL,
        M_FONTE = M.FONTE_DEBUG
    FROM MEU M
    FULL OUTER JOIN ORI O
      ON O.CODFILIAL = M.CODFILIAL
     AND O.CHAPA = M.CHAPA
     AND O.CODEVENTO = M.CODEVENTO
     AND O.COMPETENCIA = M.COMPETENCIA
    WHERE O.CHAPA IS NOT NULL
      AND M.CHAPA IS NOT NULL
      AND (
          ABS(ISNULL(M.VALOR_HORA,0) - ISNULL(O.VALOR_HORA,0)) > @TOL_VALOR_HORA
       OR ABS(ISNULL(M.VALOR_MENSAL,0) - ISNULL(O.VALOR_MENSAL,0)) > @TOL_VALOR_MENSAL
      )
),
PS AS
(
    SELECT PS.*, RN = ROW_NUMBER() OVER (PARTITION BY PS.CODCOLIGADA, PS.CHAPA, PS.CODEVENTO ORDER BY CASE WHEN PS.NROPERIODO = 40 THEN 0 WHEN PS.NROPERIODO = 10 THEN 1 ELSE 2 END, PS.RECMODIFIEDON DESC, PS.RECCREATEDON DESC)
    FROM PFFINANC PS WITH (NOLOCK)
    JOIN CHAVES C ON C.CHAPA = PS.CHAPA AND C.CODEVENTO = PS.CODEVENTO
    WHERE PS.CODCOLIGADA = @CODCOLIGADA AND PS.MESCOMP = @MES AND PS.ANOCOMP = @ANO AND PS.NROPERIODO IN (10, 40)
),
H_EVENTO AS
(
    SELECT H.*, RN = ROW_NUMBER() OVER (PARTITION BY H.CODCOLIGADA, H.CHAPA, H.CODEVENTO ORDER BY H.DTMUDANCA DESC, H.RECMODIFIEDON DESC, H.RECCREATEDON DESC)
    FROM PFHSTSAL H WITH (NOLOCK)
    JOIN CHAVES C ON C.CHAPA = H.CHAPA AND C.CODEVENTO = H.CODEVENTO
    WHERE H.CODCOLIGADA = @CODCOLIGADA AND H.DTMUDANCA < @PROXIMO_DIA_COMPETENCIA
),
SC AS
(
    SELECT SC.*, RN = ROW_NUMBER() OVER (PARTITION BY SC.CODCOLIGADA, SC.CHAPA, SC.CODEVENTO, SC.NROSALARIO ORDER BY SC.RECMODIFIEDON DESC, SC.RECCREATEDON DESC)
    FROM PFSALCMP SC WITH (NOLOCK)
    JOIN CHAVES C ON C.CHAPA = SC.CHAPA AND C.CODEVENTO = SC.CODEVENTO
    WHERE SC.CODCOLIGADA = @CODCOLIGADA AND SC.RECCREATEDON < @PROXIMO_DIA_COMPETENCIA
),
CANDIDATOS AS
(
    SELECT C.CODFILIAL, C.CHAPA, C.CODEVENTO, C.M_FONTE, C.O_VALOR_HORA, C.O_VALOR_MENSAL, 'PFFINANC_VALOR_REF' FONTE_CANDIDATA,
           CAST(COALESCE(PS.VALORORIGINAL, PS.VALOR) AS decimal(19,6)) CAND_VALOR_MENSAL,
           CAST(CASE WHEN ISNULL(PS.REF,0) > 0 THEN COALESCE(PS.VALORORIGINAL, PS.VALOR) / PS.REF ELSE NULL END AS decimal(19,6)) CAND_VALOR_HORA
    FROM CHAVES C JOIN PS ON PS.CHAPA = C.CHAPA AND PS.CODEVENTO = C.CODEVENTO AND PS.RN = 1
    UNION ALL
    SELECT C.CODFILIAL, C.CHAPA, C.CODEVENTO, C.M_FONTE, C.O_VALOR_HORA, C.O_VALOR_MENSAL, 'PFHSTSAL_ULT_EVENTO',
           CAST(H.SALARIO AS decimal(19,6)),
           CAST(CASE WHEN ISNULL(H.JORNADA,0) > 0 THEN H.SALARIO / (H.JORNADA / 60.0) ELSE H.SALARIO END AS decimal(19,6))
    FROM CHAVES C JOIN H_EVENTO H ON H.CHAPA = C.CHAPA AND H.CODEVENTO = C.CODEVENTO AND H.RN = 1
    UNION ALL
    SELECT C.CODFILIAL, C.CHAPA, C.CODEVENTO, C.M_FONTE, C.O_VALOR_HORA, C.O_VALOR_MENSAL, 'PFSALCMP_DO_HIST',
           CAST(SC.VALOR AS decimal(19,6)),
           CAST(CASE WHEN ISNULL(SC.JORNADA,0) > 0 THEN SC.VALOR / (SC.JORNADA / 60.0) ELSE SC.VALOR END AS decimal(19,6))
    FROM CHAVES C
    JOIN H_EVENTO H ON H.CHAPA = C.CHAPA AND H.CODEVENTO = C.CODEVENTO AND H.RN = 1
    JOIN SC ON SC.CHAPA = C.CHAPA AND SC.CODEVENTO = C.CODEVENTO AND SC.NROSALARIO = H.NROSALARIO AND SC.RN = 1
)
SELECT
    M_FONTE,
    FONTE_CANDIDATA,
    QTD_MATCH_AMBOS = SUM(CASE WHEN ABS(ISNULL(CAND_VALOR_HORA,0) - ISNULL(O_VALOR_HORA,0)) <= @TOL_VALOR_HORA
                                AND ABS(ISNULL(CAND_VALOR_MENSAL,0) - ISNULL(O_VALOR_MENSAL,0)) <= @TOL_VALOR_MENSAL THEN 1 ELSE 0 END),
    QTD_MATCH_HORA = SUM(CASE WHEN ABS(ISNULL(CAND_VALOR_HORA,0) - ISNULL(O_VALOR_HORA,0)) <= @TOL_VALOR_HORA THEN 1 ELSE 0 END),
    QTD_MATCH_MENSAL = SUM(CASE WHEN ABS(ISNULL(CAND_VALOR_MENSAL,0) - ISNULL(O_VALOR_MENSAL,0)) <= @TOL_VALOR_MENSAL THEN 1 ELSE 0 END),
    QTD_CANDIDATOS = COUNT(*)
FROM CANDIDATOS
GROUP BY M_FONTE, FONTE_CANDIDATA
ORDER BY QTD_MATCH_AMBOS DESC, QTD_MATCH_MENSAL DESC, QTD_MATCH_HORA DESC, M_FONTE, FONTE_CANDIDATA;
